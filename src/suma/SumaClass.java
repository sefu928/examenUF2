package suma;

public class SumaClass {
    
    public int Sumar(int n1, int n2){
        return n1 + n2;
    }
    
    public int getNum1(int num1){
        return num1;
        
    }
    
    public int sumaValorAbsolut(int n1, int n2){
        
        return Math.abs(n1) + Math.abs(n2);
    }
}
